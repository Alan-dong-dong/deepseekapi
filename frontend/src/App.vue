<script setup lang="ts">
import { ref } from 'vue'
import hljs from 'highlight.js'
import { marked } from 'marked'
import 'highlight.js/styles/atom-one-dark.css'
import 'highlight.js/lib/languages/python'
import { markedHighlight } from 'marked-highlight'

marked.use(markedHighlight({
  langPrefix: 'hljs language-',
  highlight(code, lang) {
    const language = lang || 'text'  // 如果没有指定语言，默认为 'text'
    if (lang && hljs.getLanguage(lang)) {
      const html = hljs.highlight(code, { language }).value
      // 添加自定义属性来标识语言
      return `<div data-language="${language}">${html}</div>`
    }
    return code
  }
}))

marked.setOptions({
  renderer: new marked.Renderer(),
  gfm: true,
  breaks: true,
  pedantic: false
})

interface Message {
  role: 'user' | 'assistant'
  content: string
}

const messages = ref<Message[]>([])
const newMessage = ref('')
const loading = ref(false)
const currentResponse = ref('')

const sendMessage = async () => {
  if (!newMessage.value.trim() || loading.value) return
  
  loading.value = true
  messages.value.push({
    role: 'user',
    content: newMessage.value
  })
  
  try {
    // 添加助手消息
    currentResponse.value = ''
    messages.value.push({
      role: 'assistant',
      content: ''
    })

    const response = await fetch('http://localhost:8000/api/chat', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        messages: messages.value
      })
    })

    const reader = response.body?.getReader()
    const decoder = new TextDecoder()

    if (reader) {
      while (true) {
        const { done, value } = await reader.read()
        if (done) break

        const chunk = decoder.decode(value)
        const lines = chunk.split('\n')
        
        for (const line of lines) {
          if (line.trim() && line !== 'data: [DONE]') {
            try {
              const jsonStr = line.replace(/^data: /, '')
              const data = JSON.parse(jsonStr)
              if (data.choices?.[0]?.delta?.content) {
                const content = data.choices[0].delta.content
                currentResponse.value += content
                messages.value[messages.value.length - 1].content = currentResponse.value
              }
            } catch (e) {
              // 忽略解析错误
            }
          }
        }
      }
    }

  } catch (error: any) {
    console.error('Error details:', error)
    messages.value.push({
      role: 'assistant',
      content: '抱歉，发生了错误，请稍后重试。'
    })
  } finally {
    loading.value = false
    newMessage.value = ''
  }
}

// 添加复制代码功能
const copyCode = (event: MouseEvent) => {
  const button = event.target as HTMLElement
  const pre = button.closest('pre')
  if (pre) {
    const code = pre.querySelector('code')
    if (code) {
      navigator.clipboard.writeText(code.textContent || '')
      button.textContent = '已复制!'
      setTimeout(() => {
        button.textContent = '复制'
      }, 2000)
    }
  }
}

// 修改格式化函数
const formatMessage = (content: string) => {
  try {
    // 使用 marked.parse() 并强制类型为 string
    const html = marked.parse(content) as string
    return html.replace(/<pre><code/g, '<pre><button class="copy-btn" onclick="window.copyCode(event)">复制</button><code')
  } catch (e) {
    console.error('Markdown parsing error:', e)
    return content
  }
}

// 暴露复制函数到 window 对象
window.copyCode = copyCode
</script>

<template>
  <div class="container">
    <h1>AI 智能助手</h1>
    
    <div class="chat-container">
      <div class="messages">
        <div v-for="(msg, index) in messages" 
             :key="index" 
             :class="['message', msg.role]">
          <div class="avatar">{{ msg.role === 'user' ? '👤' : '🤖' }}</div>
          <div class="content" v-html="formatMessage(msg.content)"></div>
        </div>
      </div>
      
      <div class="input-area">
        <textarea 
          v-model="newMessage" 
          placeholder="请输入您的问题..."
          @keyup.ctrl.enter="sendMessage"
          :disabled="loading"
        ></textarea>
        <button 
          @click="sendMessage" 
          :disabled="loading || !newMessage.trim()"
        >
          {{ loading ? '思考中...' : '发送' }}
        </button>
      </div>
    </div>
  </div>
</template>

